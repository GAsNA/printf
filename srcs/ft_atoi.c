#include "../headers/ft_printf.h"

int	ft_atoi(char *s)
{
	int	res;
	int	i;

	res = 0;
	i = -1;
	while (s[++i] >= '0' && s[i] <= '9')
	{
		res *= 10;
		res += s[i] - '0';
	}
	return (res);
}
